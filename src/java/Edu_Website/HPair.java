package Edu_Website;

import java.util.ArrayList;

public class HPair {
    String id = "";
    String dataA = "";
    String dataB = "";
    
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HPair(){}
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HPair(String id, String data){
        this.id = id;
        this.dataA = data;
        
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HPair(String id, String dataA, String dataB){
        this.id = id;
        this.dataA = dataA;
        this.dataB = dataB;
        
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HPair(HPair data){
        this.id = data.getID();
        this.dataA = data.getDataA();
        this.dataB = data.getDataB();
        
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getID(){
        return this.id;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getDataA(){
        return this.dataA;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getDataB(){
        return this.dataB;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setID(String id){
        this.id = id;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setDataA(String dataA){
        this.dataA = dataA;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setDataB(String dataB){
        this.dataB = dataB;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public ArrayList<HPair> sortByRank(ArrayList<HPair> data){
        ArrayList<HPair> result = new ArrayList<>();
        int bigestIndex = 0;
        HPair bigestrank= new HPair();
        bigestrank.setID("0");
        
        while(data.size()>0){
            for(int i = 0; i < data.size(); i++){
                if(Integer.parseInt(bigestrank.getID()) < Integer.parseInt(data.get(i).getID())){
                    bigestIndex = i;
                    bigestrank = data.get(i);
                }
            }
            result.add(data.get(bigestIndex));
            data.remove(bigestIndex);
            bigestrank.setID("0");
        }
        
        return result;
    }
}
